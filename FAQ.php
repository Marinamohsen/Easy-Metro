You will find your validated tickets under Menu: Tickets > My Tickets. If your ticket is not displayed, please update “My    Tickets.” Also check that you are logged in to the app. Tickets purchased when you were logged in are only displayed in the login in mode.</p>
     <p> <h4>4.2 What happens if my battery is empty?</h4>
          If you cannot prove that you bought a ticket due to a technical problem with your mobile phone (this includes         battery empty) the ticket inspector has the right to fine you for travelling without a valid ticket under the        provisions of the public transportation and tariff regulations. If you cannot or are unable to completely download the mobile ticket to your device, you must purchase a ticket from another point of sale prior to embarking on your journey.  </p>
<p> <h4>4.3 What happens if there are interruptions to the mobile network or the service becomes unavailable?</h4>
The purchase and download of the mobile ticket must be completed prior to starting the journey on the public transportation services provided by the transportation companies (LVB, HAVAG, DB Regio, SWB Bus and Rail). The tickets are saved on the mobile end device. Network interruptions therefore do not affect their use.</p>
